package bgu.spl.app;

public class ShoeStorageInfo {
	private String fShoeType;
	private int fAmountOnStorage;
	private int fDiscountedAmount;
	
	public String getShoeType() {
		return fShoeType;
	}
	public void setShoeType(String fShoeType) {
		this.fShoeType = fShoeType;
	}
	
	public int getAmountOnStorage() {
		return fAmountOnStorage;
	}
	public void setAmountOnStorage(int fAmountOnStorage) {
		this.fAmountOnStorage = fAmountOnStorage;
	}
	
	public int getDiscountedAmount() {
		return fDiscountedAmount;
	}
	public void setDiscountedAmount(int fDiscountedAmount) {
		this.fDiscountedAmount = fDiscountedAmount;
	}
	
}
